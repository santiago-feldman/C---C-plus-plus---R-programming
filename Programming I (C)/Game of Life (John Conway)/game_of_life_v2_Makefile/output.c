void output_world(void)
{
    int i, j;
    for ( i = 0; i < HEIGHT; ++i)
    {
        for ( j = 0; j < WIDTH; ++j)
        {
            printf("|%c", world[i][j]);
        }
        printf("|\n");
    }
} 